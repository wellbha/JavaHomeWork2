public class SumOfNaturalNum5 {

    public static void main(String[] args) {

        int max = 10;

        int total = 0;
        for ( int i = 1; i < max ; i++)
            total = total + i;
            System.out.print("The sum of first 10 natural number is: ");
            System.out.println(total);

    }
}
